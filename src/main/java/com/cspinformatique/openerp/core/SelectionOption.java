package com.cspinformatique.openerp.core;

public class SelectionOption {
	public final String code;
	public final String value;

	/**
	 * Default constructor
	 * 
	 * @param code
	 * @param value
	 */
	public SelectionOption(final String code, final String value) {
		this.code = code;
		this.value = value;
	}
}
